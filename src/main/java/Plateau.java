public class Plateau
{
   private int max_x;
   private int max_y;

   public void setMax_x(int x)
   {
       this.max_x=x;
   }
    public void setMax_y(int y)
    {
        this.max_y=y;
    }

    public int getMax_x()
    {
        return max_x;
    }

    public int getMax_y()
    {
        return max_y;
    }

}
